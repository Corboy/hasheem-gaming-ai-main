function coverPath(coverSlug: string): string {
  return `/store-covers/${coverSlug}.jpg`;
}

function buildDescription(seed: RequestedGameSeed): string {
  return `${seed.title} is one of the most popular ${seed.genre.toLowerCase()} titles. ${seed.blurb}`;
}

export const games: Game[] = requestedGameSeeds.map((seed) => {
  const image = coverPath(seed.coverSlug);

  return {
    id: seed.id,
    title: seed.title,
    image,
    screenshots: [image],
    price: seed.price,
    originalPrice: seed.originalPrice,
    rating: seed.rating,
    genre: seed.genre,
    platforms: seed.platforms,
    description: buildDescription(seed),
    releaseDate: seed.releaseDate,
    developer: seed.developer,
    publisher: seed.publisher,
    features: seed.features,
  };
});

export function getGameById(id: string): Game | undefined {
  return getGameByIdFromList(games, id);
}

export function getGamesByPlatform(platform: Platform | "All"): Game[] {
  return getGamesByPlatformFromList(games, platform);
}

export function getGameByIdFromList(list: Game[], id: string): Game | undefined {
  return list.find((game) => game.id === id);
}

export function getGamesByPlatformFromList(
  list: Game[],
  platform: Platform | "All",
): Game[] {
  if (platform === "All") {
    return list;
  }

  return list.filter((game) => game.platforms.includes(platform));
}
